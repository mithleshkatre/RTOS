void HPTask(void * argument){
	char *data="HPT...........................\r\n";
	while(1){
		char *str= "\nEnter HPT \r\n";
		HAL_UART_Transmit(&huart2, str, strlen(str), HAL_MAX_DELAY);

		send_uart(data);

		char *str1= "Exit HPT \r\n\n\n";
		HAL_UART_Transmit(&huart2, str1, strlen(str1), HAL_MAX_DELAY);

		vTaskDelay(750);
	}
}


