void ADCs(void *argument){
	while(1){
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
	ADC_val= HAL_ADC_GetValue(&hadc1);
	HAL_ADC_Stop(&hadc1);

	vTaskDelay(2000);
	}
}


void DHT11(void *argument){

	while(1){
		if(xQueueSemaphoreTake(binsima, 2500)!=pdFALSE){
			HAL_UART_Transmit(&huart2, "failed", 7, HAL_MAX_DELAY);
		}
		else{

			DHT11_Get_Data(Temperature, Humidity);
			HAL_UART_Transmit(&huart2, Temperature, strlen(Temperature), 400);
			HAL_UART_Transmit(&huart2, Humidity, strlen(Humidity), 400);
		}
	}

}
void SD(void *argument){
	uint8_t index=0;
	while(1){
		char *buffer= pvPortMalloc(50*sizeof(char));
		sprintf(buffer,"%d.   ADC = %d \r\n",index,ADC_val);
		Mount_SD("/");
		Update_File("ADC_DATA.TXT", buffer);
		sprintf(buffer,"%d.  Temp= %d  humidity=%d  \r\n ",index,Temperature,Humidity);
		Update_File("TEMP.TXT", buffer);
		vPortFree(buffer);
		Unmount_SD("/");

		index++;

		vTaskDelay(3000);
	}
}

